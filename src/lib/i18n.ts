// Default to "en" for SSR consistency — only read localStorage after mount
let currentLang = "en";
let clientHydrated = false;

export function setLanguage(lang: string) {
  currentLang = lang;
  if (typeof window !== "undefined") {
    localStorage.setItem("sre-language", lang);
  }
}

export function getLanguage(): string {
  if (typeof window !== "undefined") {
    if (!clientHydrated) {
      clientHydrated = true;
      const stored = localStorage.getItem("sre-language");
      if (stored && translations[stored]) {
        currentLang = stored;
      }
    }
    return currentLang;
  }
  return "en";
}

export function t(key: string, params?: Record<string, string | number>): string {
  const lang = getLanguage();
  const value = translations[lang]?.[key] || translations["en"]?.[key] || key;
  if (!params) return value;
  return Object.entries(params).reduce(
    (acc, [k, v]) => acc.replace(new RegExp(`\\{${k}\\}`, "g"), String(v)),
    value
  );
}

export function useTranslation() {
  return { t, setLanguage, getLanguage };
}
